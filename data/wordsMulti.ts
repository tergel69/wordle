// Export all word lists by length
export const WORDS_BY_LENGTH: Record<number, readonly string[]> = {
  3: [...WORDS_3],
  4: [...WORDS_4],
  5: [...WORDS_5],
  6: [...WORDS_6],
};

// Get random word for a given length
export function getWordByLength(length: number): string {
  const words = WORDS_BY_LENGTH[length];
  if (!words || words.length === 0) {
    // Fallback to 5-letter words if no words for length
    return WORDS_5[Math.floor(Math.random() * WORDS_5.length)];
  }
  return words[Math.floor(Math.random() * words.length)];
}

// Get daily word for a given length
export function getDailyWordByLength(length: number, date: Date = new Date()): string {
  const words = WORDS_BY_LENGTH[length];
  if (!words || words.length === 0) {
    return WORDS_5[Math.floor(Math.random() * WORDS_5.length)];
  }
  // Use epoch date of Jan 1, 2024
  const epochDate = new Date('2024-01-01');
  const daysSinceEpoch = Math.floor(
    (date.getTime() - epochDate.getTime()) / (1000 * 60 * 60 * 24)
  );
  const index = daysSinceEpoch % words.length;
  return words[index];
}

// Check if a word is valid for any length
export function isValidWord(word: string): boolean {
  const length = word.length;
  if (length < 3 || length > 9) return false;
  
  const words = WORDS_BY_LENGTH[length];
  if (!words) return false;
  
  return words.includes(word.toUpperCase());
}
